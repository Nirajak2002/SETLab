def Extended(a,b):
    r1=a
    r2=b
    t1=0
    t2=1
    while(r2>0):
        q = r1 // r2
        r = r1 % r2
        t = t1 - q * t2
        r1 = r2
        r2 = r
        t1 = t2
        t2 = t
    

    if(t1<0):
        return t1+a
    
    return t1


a= int(input("Enter number M: "))
b= int(input("Enter number A: "))


inverse = Extended(a,b)
print("Multiplicative modular inverse - %d" %(inverse))

